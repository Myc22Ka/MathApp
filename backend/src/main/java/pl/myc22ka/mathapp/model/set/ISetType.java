package pl.myc22ka.mathapp.model.set;

/**
 * Enum for all set types in entire App.
 *
 * @author Myc22Ka
 * @version 1.0.0
 * @since 2025.06.19
 */
public enum ISetType {
    /**
     * Finite set type.
     */
    FINITE,
    /**
     * Interval set type.
     */
    INTERVAL,
    /**
     * Fundamental set type.
     */
    FUNDAMENTAL,
    /**
     * Reduced fundamental set type.
     */
    REDUCED_FUNDAMENTAL
}
